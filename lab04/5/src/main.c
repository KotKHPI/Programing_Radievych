int main() {
	float x = -0.2; // записуємо значення х
	float y;

	if (x <= -1) {
	y = -x -1; //значення функції на проміжку х <= -1
}
	if (x > -1 && x <= 0) {
	y = x + 1; //значення функції на проміжку від х > -1 до x <= 0
}
	if (x > 0 && x < 1) {
	y = -x + 1; //значення функції на проміжку від х > 0 до x < 1
}
	if (x >= 1) {
	y = x - 1; //значення функції на проміжку х >= 1
}

	return 0;
}
