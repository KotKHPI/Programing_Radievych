#define N 439
int main()
{
	int n1 = N/100; //вираження першого числа
	int n2 = N%100%10; // вирадення третього числа
	float r; // порожня змінна
	
	r = (float) n1/n2; // у скільки разів перше число більше, ніж третє
	
	float n3 = r * 100.0; // перенос двух чисел до цілої частини
	int n4 = n3; // присвоєння зімінній n3 натуральних значень
	float n5 = n4 / 100.0; //виделення двох чисел після коми
	
	return 0;
}
