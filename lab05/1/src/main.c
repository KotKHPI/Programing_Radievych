int main() {
	int a[2] = {3, 10}; //вписуємо два значення, НСД яких ми бажаємо знайти
	while (a[0] != a[1]) { // запуск циклу
		if (a[0] > a[1]) { // віднимаємо найменше з двох чисел
		a[0] = a[0] - a[1];
		} else {
		a[1] = a[1] - a[0]; // віднимаємо найменше з двох чисел
		}
	} /*a[0] - це НСД, яке ми отривали в наслідок поступового віднімання двох чисел,
до поки вони не стали тотожними (алгоритм Евкліда)*/
	return 0;
}
